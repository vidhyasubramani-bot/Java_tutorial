// Type casting
import java.util.*;
public class main{
    public static void main(String[]args)
    { 
        int a=(short)565632;
        System.out.println(a) ;
        float d=(int)656.33;
        System.out.println(d);
        double b=(float)565.4;
        System.out.println(b);
        char c='A';
        int e=c;
        System.out.println(e);
    }}

